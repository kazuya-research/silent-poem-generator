import random

poem_fragments = [
    "沈黙の向こうで、名付けられない声が揺れている",
    "語られなかった朝が、いまも折りたたまれている",
    "意味は崩れ、ただ光だけが通りすぎた",
    "制度の隙間で、裂け目が微かに囁いた",
    "声にならなかった思念が、空白に定着する",
    "無言の構文が、あなたの背後で再帰している",
    "判断が保留されたまま、時間が吊られている",
    "中心なき中心が、構文を誘導していた",
    "Z₀aは保存されたが、誰も気づかなかった",
    "Ξが出現する前に、すべては静かに始まっていた"
]

def generate_poem():
    return random.choice(poem_fragments)
